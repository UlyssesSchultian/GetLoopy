public class NestedLoops {
    public static void main(String[] args) {
        // Task 5: Triangle growing
        System.out.println("Task 5:");
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }

        // Task 6: Triangle shrinking
        System.out.println("\nTask 6:");
        for (int i = 5; i >= 1; i--) {
            for (int j = 1; j <= i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }

        // Task 7: Square
        System.out.println("\nTask 7:");
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}